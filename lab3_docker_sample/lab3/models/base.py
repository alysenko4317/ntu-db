from sqlalchemy.orm import declarative_base

# Працюючи з ORM ми описуємо структуру бази даних і водночас визначаємо класи,
# які мають сутності предметної області. Це робиться за допомогою підсистеми declarative.
# У SQLAlchemy вона ініціалізується шляхом створення класу, який потім використовується
# як базовий для інших класів-моделей:
myBase = declarative_base()
